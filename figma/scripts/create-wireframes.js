/**
 * create-wireframes.js
 * Reads Claude's figma-import.json and creates frames in Figma via REST API
 *
 * Usage: node create-wireframes.js ../../outputs/wireframes/figma-import.json
 */

require('dotenv').config();
const fs = require('fs');
const path = require('path');

const FIGMA_API_TOKEN = process.env.FIGMA_API_TOKEN;
const FIGMA_FILE_KEY  = process.env.FIGMA_FILE_KEY;

if (!FIGMA_API_TOKEN || !FIGMA_FILE_KEY) {
  console.error('❌  Missing FIGMA_API_TOKEN or FIGMA_FILE_KEY in .env');
  process.exit(1);
}

const inputFile = process.argv[2];
if (!inputFile) {
  console.error('❌  Usage: node create-wireframes.js <path-to-figma-import.json>');
  process.exit(1);
}

const spec = JSON.parse(fs.readFileSync(path.resolve(inputFile), 'utf8'));

async function createWireframes() {
  console.log(`🎨  Creating wireframes for: ${spec.project}`);
  console.log(`📄  Target page: ${spec.page}`);
  console.log(`🖼   Frames to create: ${spec.frames.length}`);

  // Figma Plugin API nodes to create
  const nodes = spec.frames.map((frame, i) => ({
    type: 'FRAME',
    name: frame.name,
    x: i * (frame.width + 80),
    y: 0,
    width: frame.width,
    height: frame.height || 900,
    fills: [{ type: 'SOLID', color: hexToFigmaRGB(frame.background || '#F5F5F5') }],
    children: (frame.components || []).map(comp => ({
      type: comp.type || 'RECTANGLE',
      name: comp.name,
      x: comp.x,
      y: comp.y,
      width: comp.width,
      height: comp.height,
      fills: [{ type: 'SOLID', color: hexToFigmaRGB(comp.fill || '#E0E0E0') }],
    })),
  }));

  // Note: Figma REST API does not support node creation directly.
  // This outputs a Figma Plugin script that can be run inside Figma.
  const pluginScript = generatePluginScript(nodes, spec.page);

  const outputPath = path.join(path.dirname(inputFile), 'figma-plugin-script.js');
  fs.writeFileSync(outputPath, pluginScript);

  console.log(`\n✅  Figma plugin script generated: ${outputPath}`);
  console.log('\n📋  To use:');
  console.log('  1. Open your Figma file');
  console.log('  2. Plugins → Development → Run last plugin');
  console.log('  3. Or paste script into Figma\'s Plugin Console');
}

function generatePluginScript(nodes, pageName) {
  return `
// Auto-generated by Claude Code UX Automation
// Run this in Figma's Plugin console or as a plugin

const pageName = "${pageName}";

// Find or create the target page
let page = figma.root.children.find(p => p.name === pageName);
if (!page) {
  page = figma.root.appendChild(figma.createPage());
  page.name = pageName;
}
figma.currentPage = page;

const frames = ${JSON.stringify(nodes, null, 2)};

frames.forEach(frameSpec => {
  const frame = figma.createFrame();
  frame.name = frameSpec.name;
  frame.x = frameSpec.x;
  frame.y = frameSpec.y;
  frame.resize(frameSpec.width, frameSpec.height);
  frame.fills = frameSpec.fills;

  (frameSpec.children || []).forEach(childSpec => {
    const rect = figma.createRectangle();
    rect.name = childSpec.name;
    rect.x = childSpec.x;
    rect.y = childSpec.y;
    rect.resize(childSpec.width, childSpec.height);
    rect.fills = childSpec.fills;
    frame.appendChild(rect);
  });

  page.appendChild(frame);
  console.log('Created frame:', frame.name);
});

figma.closePlugin('✅ Wireframes created!');
`;
}

function hexToFigmaRGB(hex) {
  const result = /^#?([a-f\\d]{2})([a-f\\d]{2})([a-f\\d]{2})$/i.exec(hex);
  return result ? {
    r: parseInt(result[1], 16) / 255,
    g: parseInt(result[2], 16) / 255,
    b: parseInt(result[3], 16) / 255,
  } : { r: 0.94, g: 0.94, b: 0.94 };
}

createWireframes().catch(console.error);
